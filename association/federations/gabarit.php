        <h2>ÔCampus</h2>
        <div class="bloc-paragraphes">
            <p>
                ÔCampus est la fédération des associations étudiantes de l’Université d’Orléans et des autres
                établissements d’enseignement supérieur de la Métropole d’Orléans.
            </p>
            <p>
                Elle confédère une trentaine d’associations, qu’elle représente auprès des collectivités territoriales,
                des institutions publiques, et des autres acteurs de l’enseignement supérieur afin de défendre les
                droits et les intérêts des étudiants. Ses objectifs comprennent aussi la mise en valeur et l’entretien
                de la vie associative étudiante, avec notamment des événements de cohésion mettant en concours tout le
                réseau, ainsi que des formations sur la gestion et les actions d’une association étudiante et son rôle
                dans les problématiques étudiantes et jeunesse.
            </p>
            <p>
                Elle s’interdit toute prise de position partisane ou syndicale, et s’interdit également toute ingérence
                dans la gestion interne de ses associations membres, qui restent souveraines.
            </p>
