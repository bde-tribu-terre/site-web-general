<h1>Pourquoi adhérer ?</h1>
<hr>
<div class="row">
    <div class="col-sm-2"></div>
    <div class="col-sm-8">
        <div class="bloc-paragraphes">
            <p>
                Adhérer à Tribu-Terre, c'est payer 5€ pour bénéficier de divers avantages pendant toute l'année
                universitaire en cours.
            </p>
            <p>
                Une fois adhérent, il vous sera décerné une carte nominative attestant votre adhésion. C'est sur
                présentation de cette dernière que vous pourrez profiter des avantages.
            </p>
        </div>
        <h2>Chez nos partenaires</h2>
        <div class="bloc-paragraphes">
            <p>
                L'association Tribu-Terre négocie chaque année des partenariats. Actuellement, trois partenaires
                permettent aux adhérents de bénéficier de tarifs préférentiels sur leur carte.
            </p>
            <p>
                <a href="/association/partenaires/">
                    Plus de détails sur les partenaires et les tarifs préférentiels.
                </a>
            </p>
        </div>
        <h2>Pour nos événements</h2>
        <div class="bloc-paragraphes">
            <p>
                Les événements organisés par Tribu-Terre sont toujours ouverts à tous. Malgré tout, les adhérents
                disposent de différents avantages dépendants de la nature de l'événement.
            </p>
            <p>
                Lors des soirées organisées par l'association, une "pré-vente" sera proposée et permettra lors de la
                soirée de profiter de tarifs préférentiels sur les consommations (notamment au sein d'établissements qui
                ne sont pas partenaires à l'année avec Tribu-Terre). Dans ce cas-là, le prix de cette prévente sera
                inférieure sur présentation de la carte d'adhérent.
            </p>
            <p>
                Chaque hiver, Tribu-Terre s'associe avec d'autres associations étudiantes du campus pour organiser un
                week-end au ski. Ici, bien que le prix du voyage soit le même pour tous, les adhérents sont prioritaires
                sur les non-adhérents s'il y a plus de demandes que de places.
            </p>
            <p>
                <a href="/events/">
                    Voir la liste des événements organisés prochainement.
                </a>
            </p>
        </div>
        <h2>Pour nos goodies</h2>
        <div class="bloc-paragraphes">
            <p>
                Votre association Tribu-Terre vend différents goodies au cours de l'année universitaire, dans le but de
                s'autofinancer ainsi que pour permettre une rivalité très amicale entre les différentes associations
                étudiantes existantes, contribuant à la vie de campus.
            </p>
            <p>
                Pour chacun d'entre eux, il existe un prix pour les non-adhérents et un prix qui est inférieur pour les
                adhérents sur présentation de la carte d'adhérent lors de la transaction.
            </p>
            <p>
                <a href="/goodies/">
                    Voir la liste des goodies actuellement disponibles.
                </a>
            </p>
        </div>
    </div>
